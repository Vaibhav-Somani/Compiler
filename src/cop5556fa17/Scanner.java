	public static enum State{
		START, AFTER_EQ, IN_IDENT, IN_DIGIT, AFTER_GREATER, AFTER_LESS, AFTER_MINUS, AFTER_MULTIPLY, AFTER_EXCL, AFTER_RETURN, AFTER_DIV, AFTER_COMMENT, IN_STRING;
	}	
	
