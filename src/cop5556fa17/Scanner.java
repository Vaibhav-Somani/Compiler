		values.put("cart_y", Kind.KW_cart_y);
		values.put("polar_a", Kind.KW_polar_a);
		values.put("polar_r", Kind.KW_polar_r);
		values.put("abs", Kind.KW_abs);
		values.put("sin", Kind.KW_sin);
		values.put("cos", Kind.KW_cos);
		values.put("atan", Kind.KW_atan);
		values.put("log", Kind.KW_log);
		values.put("image", Kind.KW_image);
		values.put("int", Kind.KW_int);
		values.put("boolean", Kind.KW_boolean);
		values.put("url", Kind.KW_url);
		values.put("file", Kind.KW_file);
		values.put("true", Kind.BOOLEAN_LITERAL);
		values.put("false", Kind.BOOLEAN_LITERAL);
		
		/* TODO  Replace this with a correct and complete implementation!!! */
		int pos = 0;
